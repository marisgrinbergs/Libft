#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	char	*tab;

	i = 0;
	while (src[i])
		i++;
	if (!(tab = (char*)malloc(sizeof(char) * (i + 1))))
		return (NULL);
	i = 0;
	while (src[i])
	{
		tab[i] = src[i];
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
